    public void initLogger(String name) {
        System.setProperty("java.util.logging.SimpleFormatter.format", "%2$s - %5$s%6$s%n");
        logger = Logger.getLogger(name);
        logger.setUseParentHandlers(false);
        handler = new ConsoleHandler();
        logger.addHandler(handler);
        
        CoreSettings core = SettingsManager.getSettingsStore("core");
        currentLevel = core.getLogLevel();
        setLogLevel(currentLevel);
    }
    
    public int getLogLevel() {
        return jComboBoxLogLevel.getSelectedIndex();
    }
    
    private Level getLogLevelType(int level) {
        Level logLevel = Level.FINEST;
        switch (level) {
            case 1: logLevel = Level.INFO; break;
            case 2: logLevel = Level.WARNING; break;
            case 3: logLevel = Level.SEVERE; break;
        }
        return logLevel;
    }
    
    public void setLogLevel(int level) {        
        jComboBoxLogLevel.setSelectedIndex(level);        
        Level logLevel = getLogLevelType(level);
        logger.setLevel(logLevel);
        handler.setLevel(logLevel);
        logger.log(Level.SEVERE, "Log level changed to " + jComboBoxLogLevel.getItemAt(level).toString());
    }

