    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Path animPath = PathHelpers.getBasePath().resolve(fileButton4.getFilePath());
        if (!PathHelpers.createPathIfRequred(animPath)) return;
        try {
            spellAnimationManager.exportDisassembly(animPath, spellAnimationLayoutPanel.getSpellAnimation());
        } catch (Exception ex) {
            Console.logger().log(Level.SEVERE, null, ex);
            Console.logger().severe("ERROR Spell Animation disasm could not be exported to : " + animPath);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        Path animPath = PathHelpers.getBasePath().resolve(fileButton1.getFilePath());
        Path spellGraphicPath = PathHelpers.getBasePath().resolve(fileButton2.getFilePath());
        Path spellPalettePath = PathHelpers.getBasePath().resolve(fileButton3.getFilePath());
        Path backgroundPath = PathHelpers.getBasePath().resolve(fileButton5.getFilePath());
        Path groundPath = PathHelpers.getBasePath().resolve(fileButton6.getFilePath());
        Path groundBasePalettePath = PathHelpers.getBasePath().resolve(fileButton7.getFilePath());
        Path groundPalettePath = PathHelpers.getBasePath().resolve(fileButton8.getFilePath());
        try {
            spellAnimationManager.importDisassembly(animPath, spellGraphicPath, spellPalettePath, backgroundPath, groundBasePalettePath, groundPalettePath, groundPath);
        } catch (Exception ex) {
            spellAnimationManager.clearData();
            Console.logger().log(Level.SEVERE, null, ex);
            Console.logger().severe("ERROR Spell Animation disasm could not be imported from : " + animPath);
        }
        onDataLoaded();
    }//GEN-LAST:event_jButton18ActionPerformed
    
